package com.tutorial.automation;

public class swapnum {
	
	public static void main(String[] args) {	
	int a=3,b=5,t=0;
	t=a;
	a=b;
	b=t;
	
	System.out.println("a="+a);
	System.out.println("b="+b);
	
	int x=8,y=5;
	x=x+y;
	y=x-y;
	x=x-y;
	System.out.println(x);
	System.out.println(y);
	
}
	
	
	
	
	

}
